#include<stdio.h>
int main(){
    char ch;
    scanf("%c",ch);
    if(c=='A'){
        printf("Excellent");
    }
    else if(c=='B'){
        printf("Good");
    }
    else if(c=='C'){
        printf("Average");
    }
    else if(c=='D'){
        printf("Below Average");
    }
    else if(c=='F'){
        printf("Fail");
    }
    return 0;

}